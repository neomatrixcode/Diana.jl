    precompile(Diana.Lexers.done, (Diana.Lexers.Lexer{Base.GenericIOBuffer{Array{UInt8, 1}}}, Bool,))
    precompile(Diana.Lexers.Type, (Type{Diana.Lexers.Lexer}, Base.GenericIOBuffer{Array{UInt8, 1}},))
    precompile(Diana.Tokens.startpos, (Diana.Tokens.Token,))
    precompile(Diana.Tokens.untokenize, (Diana.Tokens.Token,))
    precompile(Diana.Lexers.accept, (Diana.Lexers.Lexer{Base.GenericIOBuffer{Array{UInt8, 1}}}, Char,))
    precompile(Diana.Lexers._doret, (Char, Diana.Lexers.Lexer{Base.GenericIOBuffer{Array{UInt8, 1}}},))
    precompile(Diana.Lexers.is_identifier_start_char, (Char,))
end
